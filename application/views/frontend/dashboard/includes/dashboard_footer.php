																			var lastScrollTop = 0, delta = 5;
																			$(window).scroll(function(event){
																			var st = $(this).scrollTop();
																			
																			if(Math.abs(lastScrollTop - st) <= delta)
																			return;
																			
																			if (st > lastScrollTop){
																			// downscroll code
																			$(".header").css('opacity','0').animate(5000);
																			} else {
																			// upscroll code
																			$(".header").css('opacity','1').animate(5000);
																			}
																			lastScrollTop = st;
																			});
																							});
		});
